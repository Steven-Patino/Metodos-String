texto = "  hola mundo  "

# upper() - convierte a mayúsculas
print(texto.upper())  # "  HOLA MUNDO  "

# lower() - convierte a minúsculas
print(texto.lower())  # "  hola mundo  "

# capitalize() - primera letra mayúscula
print(texto.capitalize())  # "  hola mundo  "

# title() - cada palabra con mayúscula inicial
print(texto.title())  # "  Hola Mundo  "

# strip() - elimina espacios en extremos
print(texto.strip())  # "hola mundo"

# replace() - reemplaza subcadena
print(texto.replace("mundo", "Python"))  # "  hola Python  "

# swapcase() - intercambia mayúsculas/minúsculas
print("HoLa".swapcase())  # "hOlA"